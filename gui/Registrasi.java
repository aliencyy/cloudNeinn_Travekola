    public void actionPerformed (ActionEvent e){
        
        if (e.getSource()== nextButton ){
            nama = namatext.getText().trim();
            id = idText.getText().trim();
            String l = lamatext.getText().trim();
            lamaMenginap = Double.parseDouble(l);
            String next = "";
            next+="     PEMBAYARAN"+"\n\n";
            next+="Nama           : "+nama+"\n";
            next+="Id                 : "+id+"\n";
            next+="Jenis Kamar: "+comboJenis.getItemAt(comboJenis.getSelectedIndex())+"\n";
            next+="Total            : "+ lamaMenginap * harga+"\n";
            JOptionPane.showMessageDialog(null, next, "PEMBAYARAN",
            JOptionPane.INFORMATION_MESSAGE);
        } 
            
    }
