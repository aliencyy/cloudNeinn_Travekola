    private void comboJenisActionPerformed(java.awt.event.ActionEvent evt) {                                           

        if (comboJenis.getSelectedIndex() == 0){
            Kamar kamar = new KamarStandard();
            harga = kamar.getharga();
        }
        else if (comboJenis.getSelectedIndex()==1){
            Kamar kamar = new KamarSuperior();
            harga = kamar.getharga();
        }
        else if (comboJenis.getSelectedIndex()==2){
            Kamar kamar = new KamarDeluxe();
            harga = kamar.getharga();
        }
    }                                          

